# hasattr(object, name)
# 判断一个对象里面是否有name属性或者name方法，返回BOOL值，有name特性返回True， 否则返回False。

# getattr(object, name[,default])
# 获取对象object的属性或者方法，如果存在打印出来，如果不存在，打印出默认值，默认值可选。

# setattr(object, name, values)
# 给对象的属性赋值，若属性不存在，先创建再赋值。